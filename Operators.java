public class Operators {
    public static void main(String[] args) {
        int a=9;
        int b=3;
        int add;
        int difference;
        int Multiplication;
        int division;
        int modulus;
        //Addition
         add=a+b;
         //difference
         difference=a-b;
         //Multiplication
         Multiplication=a*b;
         //Division
         division=a/b;
         //modulus
         modulus=a%b;

         System.out.println(add);
         System.out.println(difference);
         System.out.println(Multiplication);
         System.out.println(division);
         System.out.println(modulus);
    }
    
}
